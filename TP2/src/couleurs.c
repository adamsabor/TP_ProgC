#include <stdio.h>

struct Couleur {
    unsigned char r, g, b, a;
};

int main() {
    struct Couleur c[10] = {
        {0xef,0x78,0x12,0xff},
        {0x2c,0xc8,0x64,0xff},
        {0xaa,0x11,0x44,0xff},
        {0x10,0x20,0x30,0xff},
        {0xff,0x00,0x00,0xff},
        {0x00,0xff,0x00,0xff},
        {0x00,0x00,0xff,0xff},
        {0x12,0x34,0x56,0xff},
        {0x99,0x88,0x77,0xff},
        {0x55,0x66,0x77,0xff}
    };

    for (int i = 0; i < 10; i++) {
        printf("Couleur %d :\n", i + 1);
        printf("R: %d\nG: %d\nB: %d\nA: %d\n\n",
               c[i].r, c[i].g, c[i].b, c[i].a);
    }

    return 0;
}
